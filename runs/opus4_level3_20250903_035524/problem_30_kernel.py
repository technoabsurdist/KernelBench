# Custom CUDA kernel for fused LayerNorm
layernorm_cuda_source = """
#include <torch/extension.h>
#include <cuda_runtime.h>
#include <cuda_fp16.h>

__global__ void layernorm_forward_kernel(
    const float* __restrict__ input,
    const float* __restrict__ weight,
    const float* __restrict__ bias,
    float* __restrict__ output,
    float* __restrict__ mean,
    float* __restrict__ rstd,
    int N,
    int C,
    float eps) {
    
    int idx = blockIdx.x;
    if (idx >= N) return;
    
    // Compute mean
    float sum = 0.0f;
    for (int c = 0; c < C; c++) {
        sum += input[idx * C + c];
    }
    float m = sum / C;
    mean[idx] = m;
    
    // Compute variance
    float var = 0.0f;
    for (int c = 0; c < C; c++) {
        float diff = input[idx * C + c] - m;
        var += diff * diff;
    }
    var = var / C;
    float r = rsqrtf(var + eps);
    rstd[idx] = r;
    
    // Normalize and apply weight/bias
    for (int c = 0; c < C; c++) {
        int pos = idx * C + c;
        float normalized = (input[pos] - m) * r;
        output[pos] = normalized * weight[c] + bias[c];
    }
}

std::vector<torch::Tensor> layernorm_cuda_forward(
    torch::Tensor input,
    torch::Tensor weight,
    torch::Tensor bias,
    float eps) {
    
    auto N = input.size(0);
    auto C = input.size(1);
    
    auto output = torch::empty_like(input);
    auto mean = torch::empty({N}, input.options());
    auto rstd = torch::empty({N}, input.options());
    
    const int threads = 1;
    const int blocks = N;
    
    layernorm_forward_kernel<<<blocks, threads>>>(
        input.data_ptr<float>(),
        weight.data_ptr<float>(),
        bias.data_ptr<float>(),
        output.data_ptr<float>(),
        mean.data_ptr<float>(),
        rstd.data_ptr<float>(),
        N, C, eps
    );
    
    return {output, mean, rstd};
}
"""

