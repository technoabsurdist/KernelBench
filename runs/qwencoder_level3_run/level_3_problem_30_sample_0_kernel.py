# 2. Fused Softmax kernel
softmax_source = """
#include <torch/extension.h>
#include <cuda_runtime.h>
#include <cub/cub.cuh>

#define CUDA_1D_KERNEL_LOOP(i, n)                            \
  for (int i = blockIdx.x * blockDim.x + threadIdx.x; i < n; \
       i += blockDim.x * gridDim.x)

__global__ void softmax_kernel(const float* input, float* output, int rows, int cols) {
    int row = blockIdx.x;
    if (row >= rows) return;
    
    const float* input_row = input + row * cols;
    float* output_row = output + row * cols;
    
    // Find max value for numerical stability
    float max_val = -INFINITY;
    for (int i = threadIdx.x; i < cols; i += blockDim.x) {
        max_val = fmaxf(max_val, input_row[i]);
    }
    
    // Reduction to find maximum
    typedef cub::BlockReduce<float, 1024> BlockReduce;
    __shared__ typename BlockReduce::TempStorage temp_storage;
    float aggregate = BlockReduce(temp_storage).Reduce(max_val, cub::Max());
    __shared__ float shared_max;
    if (threadIdx.x == 0) {
        shared_max = aggregate;
    }
    __syncthreads();
    
    // Compute exponentials and sum
    float sum = 0.0f;
    for (int i = threadIdx.x; i < cols; i += blockDim.x) {
        output_row[i] = expf(input_row[i] - shared_max);
        sum += output_row[i];
    }
    
    // Reduction to find sum
    aggregate = BlockReduce(temp_storage).Reduce(sum, cub::Sum());
    __shared__ float shared_sum;
    if (threadIdx.x == 0) {
        shared_sum = aggregate;
    }
    __syncthreads();
    
    // Normalize
    for (int i = threadIdx.x; i < cols; i += blockDim.x) {
        output_row[i] /= shared_sum;
    }
}

torch::Tensor softmax_cuda(torch::Tensor input) {
    int rows = 1;
    for (int i = 0; i < input.dim() - 1; i++) {
        rows *= input.size(i);
    }
    int cols = input.size(-1);
    
    auto output = torch::zeros_like(input);
    
    const int block_size = 1024;
    const int num_blocks = rows;
    
    softmax_kernel<<<num_blocks, block_size>>>(
        input.data_ptr<float>(),
        output.data_ptr<float>(),
        rows, cols
    );
    
    return output;
}
"""

