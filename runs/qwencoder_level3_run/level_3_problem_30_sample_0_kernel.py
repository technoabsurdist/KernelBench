# 3. Window partition/reverse fusion kernel
window_ops_source = """
#include <torch/extension.h>
#include <cuda_runtime.h>

#define CUDA_1D_KERNEL_LOOP(i, n)                            \
  for (int i = blockIdx.x * blockDim.x + threadIdx.x; i < n; \
       i += blockDim.x * gridDim.x)

__global__ void window_partition_kernel(const float* x, float* windows, 
                                        int B, int H, int W, int C, int window_size) {
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    int num_windows = (H / window_size) * (W / window_size);
    int window_elements = window_size * window_size * C;
    int total_elements = num_windows * window_elements;
    
    if (idx < total_elements) {
        int window_idx = idx / window_elements;
        int elem_in_window = idx % window_elements;
        
        int window_h = window_idx / (W / window_size);
        int window_w = window_idx % (W / window_size);
        
        int pos_in_window = elem_in_window / C;
        int c = elem_in_window % C;
        
        int win_h = pos_in_window / window_size;
        int win_w = pos_in_window % window_size;
        
        int h = window_h * window_size + win_h;
        int w = window_w * window_size + win_w;
        
        windows[idx] = x[((0 * H + h) * W + w) * C + c];
    }
}

__global__ void window_reverse_kernel(const float* windows, float* x,
                                      int B, int H, int W, int C, int window_size) {
    int idx = blockIdx.x * blockDim.x + threadIdx.x;
    int total_elements = B * H * W * C;
    
    if (idx < total_elements) {
        int b = idx / (H * W * C);
        int remaining = idx % (H * W * C);
        int h = remaining / (W * C);
        int remaining2 = remaining % (W * C);
        int w = remaining2 / C;
        int c = remaining2 % C;
        
        int window_h = h / window_size;
        int window_w = w / window_size;
        int win_h = h % window_size;
        int win_w = w % window_size;
        
        int num_windows_h = H / window_size;
        int num_windows_w = W / window_size;
        int window_idx = window_h * num_windows_w + window_w;
        int window_elements = window_size * window_size * C;
        int elem_in_window = (win_h * window_size + win_w) * C + c;
        
        x[idx] = windows[window_idx * window_elements + elem_in_window];
    }
}

torch::Tensor window_partition_cuda(torch::Tensor x, int window_size) {
    int B = x.size(0);
    int H = x.size(1);
    int W = x.size(2);
    int C = x.size(3);
    
    int num_windows = (H / window_size) * (W / window_size);
    auto windows = torch::zeros({num_windows * B, window_size, window_size, C}, 
                                torch::dtype(torch::kFloat32).device(torch::kCUDA));
    
    const int block_size = 256;
    int total_elements = num_windows * window_size * window_size * C * B;
    const int num_blocks = (total_elements + block_size - 1) / block_size;
    
    window_partition_kernel<<<num_blocks, block_size>>>(
        x.data_ptr<float>(),
        windows.data_ptr<float>(),
        B, H, W, C, window_size
    );
    
    return windows;
}

torch::Tensor window_reverse_cuda(torch::Tensor windows, int window_size, int H, int W) {
    int B = 1; // Will be computed from windows shape
    int C = windows.size(3);
    B = windows.size(0) / ((H / window_size) * (W / window_size));
    
    auto x = torch::zeros({B, H, W, C}, torch::dtype(torch::kFloat32).device(torch::kCUDA));
    
    const int block_size = 256;
    int total_elements = B * H * W * C;
    const int num_blocks = (total_elements + block_size - 1) / block_size;
    
    window_reverse_kernel<<<num_blocks, block_size>>>(
        windows.data_ptr<float>(),
        x.data_ptr<float>(),
        B, H, W, C, window_size
    );
    
    return x;
}
"""

