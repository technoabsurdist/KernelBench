# 1. Fused GELU + Linear kernel
gelu_linear_source = """
#include <torch/extension.h>
#include <cuda_runtime.h>
#include <cmath>

#define CUDA_1D_KERNEL_LOOP(i, n)                            \
  for (int i = blockIdx.x * blockDim.x + threadIdx.x; i < n; \
       i += blockDim.x * gridDim.x)

__global__ void gelu_linear_kernel(const float* input, const float* weight, const float* bias,
                                   float* output, int M, int N, int K) {
    int row = blockIdx.y;
    int col = blockIdx.x * blockDim.x + threadIdx.x;
    
    if (row < M && col < N) {
        float sum = 0.0f;
        for (int k = 0; k < K; ++k) {
            sum += input[row * K + k] * weight[col * K + k];
        }
        if (bias != nullptr) {
            sum += bias[col];
        }
        // GELU activation: 0.5 * x * (1 + tanh(sqrt(2/Ï€) * (x + 0.044715 * x^3)))
        float cdf = 0.5f * (1.0f + tanhf(0.7978845608028654f * (sum + 0.044715f * sum * sum * sum)));
        output[row * N + col] = sum * cdf;
    }
}

torch::Tensor gelu_linear_cuda(torch::Tensor input, torch::Tensor weight, torch::Tensor bias) {
    int M = input.size(0);  // batch size
    int K = input.size(1);  // input features
    int N = weight.size(0); // output features
    
    auto output = torch::zeros({M, N}, torch::dtype(torch::kFloat32).device(torch::kCUDA));
    
    const int block_size = 256;
    const int num_blocks_x = (N + block_size - 1) / block_size;
    const int num_blocks_y = M;
    
    dim3 grid(num_blocks_x, num_blocks_y);
    dim3 block(block_size, 1);
    
    gelu_linear_kernel<<<grid, block>>>(
        input.data_ptr<float>(),
        weight.data_ptr<float>(),
        bias.defined() ? bias.data_ptr<float>() : nullptr,
        output.data_ptr<float>(),
        M, N, K
    );
    
    return output;
}
"""

